	private static boolean contains(Material[] arr, Material block) {
		for(Material m : arr) {
			if(block == m) return true;
		}
		return false;
	}
	
	public static boolean isNaturalBlock(Material block) {
		return contains(naturalBlocks, block);
	}
	
	public static boolean isEditDenyingBlock(Material block) {
		return contains(editDenyingBlocks, block);
	}
	
	public static boolean isBaseConstructionBlock(Material block) {
		if(isInteriorStageBlock(block) || isDecorationStageBlock(block)) return false;
		return true;
	}
	
	public static boolean isInteriorStageBlock(Material block) {
		return contains(interiorFinishingStageBlocks, block) || contains(glassBlocks, block) || contains(glassPanes, block);
	}
	
	public static boolean isDecorationStageBlock(Material block) {
		return contains(decorationBlocks, block) || contains(redstoneComponentBlocks, block) || contains(carpetBlocks, block);
	}
	
	public static boolean isSignBlock(Material block) {
		return contains(signBlocks, block);
	}
	
	public static BlockData getSubstituteBlock(BlockData source) {
		Material mat = source.getMaterial();
		if(contains(woolBlocks, mat) || contains(terracottaBlocks, mat)) return Material.BRICKS.createBlockData();
		if(contains(glazedTerracottaBlocks, mat)) return Material.TERRACOTTA.createBlockData();
		if(contains(concreteBlocks, mat)) return Material.STONE.createBlockData();
		return source;
	}
	
	public static boolean isGlassBlock(Material m) {
		return contains(glassBlocks, m);
	}

	public static boolean isCarpet(Material m) {
		return contains(carpetBlocks, m);
	}
	
	public static boolean isNonObstructingBlock(Material m) {
		return m.isAir() || contains(otherNonObstructingBlocks,m) || contains(carpetBlocks, m) || contains(signBlocks, m);
	}
